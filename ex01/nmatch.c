#define EXIT(X, Y) (!*X && !*Y) || (*Y != '*' && *X != *Y)
#define OR(X, Y) (nmatch(X+(*Y!='*'),Y+1) + (*X && *Y=='*' ? nmatch(X+1,Y) : 0))

int	nmatch(char *s1, char *s2)
{
	return (EXIT(s1, s2) ? (!*s1 && !*s2) : OR(s1, s2));
}
