int	match(char *s1, char *s2)
{
	if ((!*s1 && !*s2) || (!*s1 && *s2 != '*'))
		return (!*s1 && !*s2) ? 1 : 0;
	if ((!*s1 && *s2 == '*') || (*s2 == '*' && *(s2 + 1) == '*'))
		return match (s1, s2 + 1);
	if (*s2 != '*')
		return (*s2 == *s1) ? match(s1 + 1, s2 + 1) : 0;
	while (*s1 && match(s1, s2 + 1) == 0)
		s1++;
	return match(s1, s2 + 1);
}
